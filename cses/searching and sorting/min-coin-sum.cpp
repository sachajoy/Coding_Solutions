void solve()
{
    int n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++)
        cin >> a[i];
    sort(a, a + n );
    ll curr = 0;
    for (int i = 0; i < n; i++)
    {
        if (curr + 1 < a[i])
            break;
        curr += a[i];
    }
    cout << curr + 1;
}
